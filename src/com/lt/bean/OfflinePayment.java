package com.lt.bean;

public class OfflinePayment {
	private float cashAmount;
	private int chequeNumber;
	
	public final float getCashAmount() {
		return cashAmount;
	}
	public final void setCashAmount(float cashAmount) {
		this.cashAmount = cashAmount;
	}
	public final int getChequeNumber() {
		return chequeNumber;
	}
	public final void setChequeNumber(int chequeNumber) {
		this.chequeNumber = chequeNumber;
	}
	
	
}
